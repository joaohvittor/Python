dia= str(input('Informe o dia da semana em numero: \n'))
if dia =="1":
    print('Domingo')
elif dia =="2":
    print('Segunda')
elif dia =="3":
    print('Terça')  
elif dia =="4":
    print('Quarta') 
elif dia =="5":
    print('Quinta')   
elif dia =="6":
    print('Sexta') 
elif dia =="7":
    print('Sabado')                    
else:
    print('Voçê digitou um numero invalido')    